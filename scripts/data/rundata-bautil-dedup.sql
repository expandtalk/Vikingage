-- 1. Bevara gamla namn som sökbara alias på moderna raden.
update public.runic_inscriptions m
set alternative_signum = (
  select array(
    select distinct x
    from unnest(coalesce(m.alternative_signum, '{}'::text[]) || g.olds) x
    where x is not null and x <> m.signum
  )
)
from (select modern_sig, array_agg(distinct old_sig) as olds from _dedup group by modern_sig) g
where lower(regexp_replace(m.signum,'\s+',' ','g')) = lower(regexp_replace(g.modern_sig,'\s+',' ','g'));

-- 2. Flytta koordinat till moderna raden om den saknar.
update public.runic_inscriptions m
set coordinates = r.coordinates, coord_source = coalesce(m.coord_source,'bautil_transfer')
from _dedup p join public.runic_inscriptions r on lower(regexp_replace(r.signum,'\s+',' ','g')) = lower(regexp_replace(p.old_sig,'\s+',' ','g'))
where lower(regexp_replace(m.signum,'\s+',' ','g')) = lower(regexp_replace(p.modern_sig,'\s+',' ','g')) and m.coordinates is null and r.coordinates is not null;

-- 3. Radera gamla tomma dubbletter där moderna signumet finns (namnet bevarat i steg 1).
delete from public.runic_inscriptions r
using _dedup p
where lower(regexp_replace(r.signum,'\s+',' ','g')) = lower(regexp_replace(p.old_sig,'\s+',' ','g'))
  and exists (select 1 from public.runic_inscriptions m where lower(regexp_replace(m.signum,'\s+',' ','g')) = lower(regexp_replace(p.modern_sig,'\s+',' ','g')));
commit;
